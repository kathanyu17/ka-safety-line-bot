def clean_markdown(text):
    text = re.sub(r'\*\*(.+?)\*\*', r'\1', text)
    text = re.sub(r'(?m)^\s*\*\s+', '- ', text)
    text = text.replace('*', '')
    text = re.sub(r'(?m)^#{1,6}\s*', '', text)
    text = re.sub(r'(?m)^-{3,}\s*$', '', text)
    text = re.sub(r'\n{3,}', '\n\n', text)
    return text.strip()


